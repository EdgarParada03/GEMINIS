    private void btnCrearProgramaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearProgramaActionPerformed
        // TODO add your handling code here:
        btnCrearPrograma.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                // Obtener los datos ingresados
                String nombrePrograma = txtNombrePrograma.getText();
                String descripcionPrograma = txtDescripcionPrograma.getText();
                String duracionPrograma = txtDuracionPrograma.getText();
                String nivelPrograma = txtNivelPrograma.getText();
                String facultadPrograma = txtFacultadPrograma.getText();
                float tarifaBase = Float.parseFloat(txtTarifaBase.getText());

                // Verificar si los campos están vacíos
                if (nombrePrograma.isEmpty() || descripcionPrograma.isEmpty() || duracionPrograma.isEmpty()
                        || nivelPrograma.isEmpty() || facultadPrograma.isEmpty() || Float.isNaN(tarifaBase)) {
                    JOptionPane.showMessageDialog(rootPane, "Los campos no pueden estar vacíos.");
                    return;
                }

                // Verificar si el programa académico ya existe
                if (!guardarBaseDatos.programaExiste(nombrePrograma)) {
                    // Crear un objeto programaAcademico con los datos ingresados
                    programaAcademico programa = new programaAcademico(nombrePrograma, descripcionPrograma, duracionPrograma,
                            nivelPrograma, facultadPrograma, tarifaBase);

                    // Llamar al método guardarPrograma
                    guardarBaseDatos.guardarPrograma(programa);

                    cargarDatosPrograma();

                    txtNombrePrograma.setText("");
                    txtDescripcionPrograma.setText("");
                    txtDuracionPrograma.setText("");
                    txtNivelPrograma.setText("");
                    txtFacultadPrograma.setText("");
                    txtTarifaBase.setText("");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "El nombre del programa academico ya existe.");
                }
            }
        });

    }//GEN-LAST:event_btnCrearProgramaActionPerformed

    private void btnEliminarProgramaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProgramaActionPerformed
        // TODO add your handling code here:
        btnEliminarPrograma.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                // Obtén el nombre del programa que quieres eliminar
                String nombrePrograma = txtNombrePrograma.getText();
                if (nombrePrograma.isEmpty()) {
                    // Muestra un mensaje de error
                    JOptionPane.showMessageDialog(null, "Ingresa el nombre del programa a eliminar");
                } else {
                    // Llama al método borrarPrograma
                    eliminarBaseDatos.borrarPrograma(nombrePrograma);

                    cargarDatosPrograma();

                    txtNombrePrograma.setText("");
                }
            }
        });
    }//GEN-LAST:event_btnEliminarProgramaActionPerformed

    private void btnEditarProgramaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProgramaActionPerformed
        // TODO add your handling code here:
        btnEditarPrograma.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String nombrePrograma = txtNombrePrograma.getText();
                String descripcion = txtDescripcionPrograma.getText();
                String duracion = txtDuracionPrograma.getText();
                String nivelAcademico = txtNivelPrograma.getText();
                String facultad = txtFacultadPrograma.getText();
                float tarifaBase = Float.parseFloat(txtTarifaBase.getText());
                int selectedRow = tablePrograma.getSelectedRow();
                if (selectedRow != -1) {
                    int id = Integer.parseInt(tablePrograma.getValueAt(selectedRow, 0).toString()); // Asume que el ID está en la columna 0

                    EditarBaseDatos dbHandler = new EditarBaseDatos();
                    try {
                        dbHandler.editarProgramaAcademico("programaacademico", nombrePrograma, descripcion, duracion, nivelAcademico, facultad, tarifaBase, id);
                        // Recarga los datos después de editar
                        cargarDatosPrograma();
                    } catch (SQLException ex) {
                        ex.printStackTrace();
                    }
                }
            }
        });

        tablePrograma.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                int selectedRow = tablePrograma.getSelectedRow();
                if (selectedRow != -1) {
                    txtNombrePrograma.setText(tablePrograma.getValueAt(selectedRow, 1).toString());
                    txtDescripcionPrograma.setText(tablePrograma.getValueAt(selectedRow, 2).toString());
                    txtDuracionPrograma.setText(tablePrograma.getValueAt(selectedRow, 3).toString());
                    txtNivelPrograma.setText(tablePrograma.getValueAt(selectedRow, 4).toString());
                    txtFacultadPrograma.setText(tablePrograma.getValueAt(selectedRow, 5).toString());
                    txtTarifaBase.setText(tablePrograma.getValueAt(selectedRow, 6).toString());
                }
            }
        });

    }//GEN-LAST:event_btnEditarProgramaActionPerformed

