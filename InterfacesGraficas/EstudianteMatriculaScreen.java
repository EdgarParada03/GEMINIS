    private void btnCrearMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearMatriculaActionPerformed
        // TODO add your handling code here:

        // Obtener el programa académico seleccionado de la base de datos
        String idProgramaSeleccionado = (String) comboBoxPrograma.getSelectedItem();
        programaAcademico programaAcademico = guardarBaseDatos.obtenerProgramaAcademicoDeLaBaseDeDatos(idProgramaSeleccionado);

        // Verificar si el estudiante está en el programa
        if (!guardarBaseDatos.esEstudianteEnPrograma(estudiante.getNombre(), idProgramaSeleccionado)) {
            JOptionPane.showMessageDialog(rootPane, "No puedes matricularte en un programa al que tu curso no pertenece.");
            return;
        }
        // Obtener el descuento seleccionado
        String descuentoSeleccionado = (String) comboBoxDescuento.getSelectedItem();
        EstrategiaDescuento estrategiaDescuento = null;
        if (descuentoSeleccionado != null) {
            if (descuentoSeleccionado.equals("Descuento rendimiento")) {
                estrategiaDescuento = new DescuentoRendimientoAcademico();
            } else if (descuentoSeleccionado.equals("Descuento situacion economica")) {
                estrategiaDescuento = new DescuentoSituacionEconomica();
            }
        }

        // Si estrategiaDescuento es null, mostrar un mensaje de error y retornar
        if (estrategiaDescuento == null) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, selecciona un descuento.");
            return;
        }

        // Crear una matrícula y calcular el costo
        matricula matricula = new matricula(programaAcademico, estrategiaDescuento);
        matricula.calcularMatricula();

        // Establecer el estado de la matrícula a "PENDIENTE"
        matricula.setEstadoMatricula("PENDIENTE");

        // Guardar la matrícula usando el nombre del estudiante
        guardarBaseDatos.guardarMatriculaPorNombre(matricula, estudiante.getNombre());

        // Generar un número aleatorio único de más de 6 dígitos
        Random rand = new Random();
        int numeroAleatorio;
        do {
            numeroAleatorio = 1000000 + rand.nextInt(9000000);  // Genera un número aleatorio entre 1000000 y 9999999
        } while (numerosGenerados.contains(numeroAleatorio));
        numerosGenerados.add(numeroAleatorio);

        // Mostrar el número aleatorio en el JTextField txtPrueba
        txtPrueba.setText(Integer.toString(numeroAleatorio));
        
        txtPrecioNormal.setText(Float.toString(programaAcademico.getTarifaBase()));
        // Mostrar el costo en el JTextField
        txtCostoMatricula.setText(Float.toString(matricula.getCostoMatricula()));

        btnPagarMatricula.setEnabled(true);

        JOptionPane.showMessageDialog(rootPane, "Tu matricula esta " + matricula.getEstadoMatricula() + " por aprobacion, por favor realiza "
                + "el pago de la misma para poder continuar de todos los procesos academicos");
        cargarDatosMatricula();


    }//GEN-LAST:event_btnCrearMatriculaActionPerformed

