    public void cargarDatosMatricula() {
        GuardarBaseDatos dbHandler = new GuardarBaseDatos();
        matricula matricula = dbHandler.obtenerMatriculaEstudiante(estudiante.getNombre());

        // Verificar si matricula es null
        if (matricula == null) {
            JOptionPane.showMessageDialog(rootPane, "No se encontró la matrícula para el estudiante: " + estudiante.getNombre());
            return;
        }

        // Convertir los datos de la matrícula a Vector<Vector<String>>
        Vector<Vector<String>> tableData = new Vector<>();
        Vector<String> row = new Vector<>();
        row.add(matricula.programaAcademico.nombrePrograma);
        if (matricula.estrategiaDescuento != null) {
            if (matricula.estrategiaDescuento instanceof DescuentoRendimientoAcademico) {
                row.add("Descuento rendimiento");
            } else if (matricula.estrategiaDescuento instanceof DescuentoSituacionEconomica) {
                row.add("Descuento situacion economica");
            }
        } else {
            row.add("No hay descuento");
        }
        row.add(Float.toString(matricula.getCostoMatricula()));
        row.add(matricula.getEstadoMatricula());
        tableData.add(row);

        // Crear un Vector<String> para las columnas
        Vector<String> columnNames = new Vector<>();
        columnNames.add("Programa Académico");
        columnNames.add("Tipo de Descuento");
        columnNames.add("Costo de Matrícula");
        columnNames.add("Estado de Matrícula");

        DefaultTableModel dtm = new DefaultTableModel(tableData, columnNames);
        tableMatricula.setModel(dtm);
    }

