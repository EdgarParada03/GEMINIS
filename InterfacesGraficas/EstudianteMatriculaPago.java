    private void comboBoxTipoTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxTipoTarjetaActionPerformed
        // TODO add your handling code here:
        comboBoxTipoTarjeta.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                String item = (String) comboBoxTipoTarjeta.getSelectedItem();
                if (item.equals("Debito")) {
                    txtCVC.setEnabled(false);
                } else {
                    txtCVC.setEnabled(true);
                }
            }
        });

    }//GEN-LAST:event_comboBoxTipoTarjetaActionPerformed

