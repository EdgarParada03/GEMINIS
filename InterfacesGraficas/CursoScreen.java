    public void cargarDatosCurso() {
        MostrarBaseDatosCursos dbHandler = new MostrarBaseDatosCursos();
        try {
            String query = "SELECT cursos.idCurso, cursos.nombre"
                    + ", semestreacademico.añoAcademico, semestreacademico.periodoAcademico,cursos.idSemestre , "
                    + "programaacademico.nombrePrograma , cursos.idPrograma\n"
                    + "FROM cursos\n"
                    + "JOIN semestreacademico ON cursos.idSemestre = semestreacademico.idSemestre\n"
                    + "JOIN programaacademico ON cursos.idPrograma = programaacademico.idProgramaAcademico;";
            Vector<Vector<String>> tableData = dbHandler.getTableData(query);
            Vector<String> columnNames = dbHandler.getColumnNames(query);

            DefaultTableModel dtm = new DefaultTableModel(tableData, columnNames);
            tableCurso.setModel(dtm);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

