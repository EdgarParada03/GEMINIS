    private void btnCrearCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCursoActionPerformed
        // TODO add your handling code here:

        // Llenar el comboBox
        btnCrearCurso.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {

                // Obtener el semestre seleccionado del comboBox
                String semestreSeleccionado = (String) comboBoxSemestre.getSelectedItem();
                String programaSeleccionado = (String) comboBoxPrograma.getSelectedItem();

                // Crear un objeto ProgramaAcademico con los datos ingresados
                cursos curso = new cursos(txtNombreCurso.getText(),
                        semestreSeleccionado, programaSeleccionado);

                if (guardarBaseDatos.cursoExiste(txtNombreCurso.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "El nombre del curso ya existe. Por favor, elige otro.");
                } else {
                    // Llamar al método guardarPrograma
                    guardarBaseDatos.guardarCurso(curso);

                    cargarDatosCurso();
                }

                txtNombreCurso.setText("");

            }
        });
    }//GEN-LAST:event_btnCrearCursoActionPerformed

    private void btnEliminarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCursoActionPerformed
        // TODO add your handling code here:
        btnEliminarCurso.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                // Obtén el año académico y el periodo académico que quieres eliminar
                String nombreCurso = txtNombreCurso.getText();

                if (nombreCurso.isEmpty()) {
                    // Muestra un mensaje de error
                    JOptionPane.showMessageDialog(null, "Ingrese el nombre del curso a eliminar");
                } else {
                    // Llama al método borrarSemestre
                    eliminarBaseDatos.borrarCurso(nombreCurso);

                    cargarDatosCurso();

                    txtNombreCurso.setText("");

                }
            }
        });

    }//GEN-LAST:event_btnEliminarCursoActionPerformed

    private void btnEditarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarCursoActionPerformed
        // TODO add your handling code here:
        btnEditarCurso.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int selectedRow = tableCurso.getSelectedRow();
                if (selectedRow != -1) {
                    String nombre = txtNombreCurso.getText();
                    // Obtener el semestre seleccionado del comboBox
                    int idSemestre = Integer.parseInt((String) comboBoxSemestre.getSelectedItem());
                    int idPrograma = Integer.parseInt((String) comboBoxPrograma.getSelectedItem());
                    int idCurso = Integer.parseInt(tableCurso.getValueAt(selectedRow, 0).toString());

                    EditarBaseDatos dbHandler = new EditarBaseDatos();
                    try {
                        dbHandler.editarCurso("cursos", nombre, idSemestre, idPrograma, idCurso);
                        // Recarga los datos después de editar
                        cargarDatosCurso();
                    } catch (SQLException ex) {
                        ex.printStackTrace();
                    }

                    // Limpia los campos de texto después de guardar los cambios
                    txtNombreCurso.setText("");

                }
            }
        });

        tableCurso.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                int selectedRow = tableCurso.getSelectedRow();
                if (selectedRow != -1) {
                    txtNombreCurso.setText(tableCurso.getValueAt(selectedRow, 1).toString());
                    comboBoxSemestre.setSelectedItem(tableCurso.getValueAt(selectedRow, 4).toString());
                    comboBoxPrograma.setSelectedItem(tableCurso.getValueAt(selectedRow, 6).toString());

                }
            }
        });

    }//GEN-LAST:event_btnEditarCursoActionPerformed

